                <el-option
                  v-for="(item, i) in list_response.list_category"
                  v-bind:key="i"
                  :label="item.nombre"
                  :value="item.id_Categoria" 
                ></el-option>
              </el-select>
            </el-col>
          </el-row>
          <el-row :gutter="10">
            <el-col :span="15">
              <div class="mt-3 ">
                <span class="mb-3">Fecha Inicial:</span>
                <el-date-picker
                  v-model="model_request.model_range_initial"
                  format="dd-MM-yyyy"
